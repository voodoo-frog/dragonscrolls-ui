const mongoose = require('mongoose');

const MODELSchema = new mongoose.Schema({
  index: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  desc: {
    type: Array,
    required: true,
  },
  url: {
    type: String,
    required: true,
  },
  source_book: {
    type: String,
    required: true,
    default: 'Basic Rules',
  },
});

module.exports = mongoose.models.MODEL || mongoose.model('MODEL', MODELSchema);
